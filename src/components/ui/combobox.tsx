const Combobox = ComboboxPrimitive.Root;

function ComboboxValue({ ...props }: ComboboxPrimitive.Value.Props) {
  return <ComboboxPrimitive.Value data-slot="combobox-value" {...props} />;
}

function ComboboxTrigger({
  className,
  children,
  showClear = false,
  ...props
}: ComboboxPrimitive.Trigger.Props & { showClear?: boolean }) {
  if (!showClear) {
    return (
      <ComboboxPrimitive.Trigger
        data-slot="combobox-trigger"
        className={cn(
          "[&_svg:not([class*='size-'])]:size-5 [&>span]:truncate",
          className,
        )}
        {...props}
      >
        {children}
        <KeyboardArrowDown
          data-slot="combobox-trigger-icon"
          className="pointer-events-none -mr-1 size-5 text-muted-foreground-light"
        />
      </ComboboxPrimitive.Trigger>
    );
  }

  return (
    <div className="group/combobox-trigger-wrap relative">
      <ComboboxPrimitive.Trigger
        data-slot="combobox-trigger"
        className={cn(
          "[&_svg:not([class*='size-'])]:size-5 [&>span]:truncate",
          className,
        )}
        {...props}
      >
        {children}
        <KeyboardArrowDown
          data-slot="combobox-trigger-icon"
          className="pointer-events-none -mr-1 size-5 text-muted-foreground-light group-has-[[data-slot=combobox-trigger-clear]]/combobox-trigger-wrap:hidden"
        />
      </ComboboxPrimitive.Trigger>
      <ComboboxPrimitive.Clear
        data-slot="combobox-trigger-clear"
        className="pointer-events-auto absolute right-1.5 top-1/2 -translate-y-1/2 flex size-6 items-center justify-center rounded-sm text-muted-foreground-light hover:text-muted-foreground focus-visible:outline-none transition-colors"
        tabIndex={-1}
      >
        <Close className="pointer-events-none size-5" />
      </ComboboxPrimitive.Clear>
    </div>
  );
}

function ComboboxClear({ className, ...props }: ComboboxPrimitive.Clear.Props) {
  return (
    <ComboboxPrimitive.Clear
      data-slot="combobox-clear"
      render={<InputGroupButton variant="ghost" size="icon-xs" />}
      className={cn(className)}
      {...props}
    >
      <Close className="pointer-events-none" />
    </ComboboxPrimitive.Clear>
  );
}

function ComboboxInput({
  className,
  children,
  disabled = false,
  showTrigger = true,
  showClear = false,
  ...props
}: ComboboxPrimitive.Input.Props & {
  showTrigger?: boolean;
  showClear?: boolean;
}) {
  return (
    <InputGroup className={cn("w-auto", className)}>
      <ComboboxPrimitive.Input
        render={<InputGroupInput disabled={disabled} />}
        {...props}
      />
      <InputGroupAddon>
        <SearchIcon className="text-muted-foreground-light" />
      </InputGroupAddon>
      <InputGroupAddon align="inline-end">
        {showTrigger && (
          <InputGroupButton
            size="icon-xs"
            variant="ghost"
            asChild
            data-slot="input-group-button"
            className="group-has-data-[slot=combobox-clear]/input-group:hidden data-pressed:bg-transparent"
            disabled={disabled}
          >
            <ComboboxTrigger />
          </InputGroupButton>
        )}
        {showClear && <ComboboxClear disabled={disabled} />}
      </InputGroupAddon>
      {children}
    </InputGroup>
  );
}

function ComboboxContent({
  className,
  side = "bottom",
  sideOffset = 6,
  align = "start",
  alignOffset = 0,
  anchor,
  container,
  ...props
}: ComboboxPrimitive.Popup.Props &
  Pick<
    ComboboxPrimitive.Positioner.Props,
    "side" | "align" | "sideOffset" | "alignOffset" | "anchor"
  > & {
    container?: HTMLElement | null;
  }) {
  const dialogPortalContainer = useDialogPortalContainer();

  return (
    <ComboboxPrimitive.Portal
      container={container ?? dialogPortalContainer ?? undefined}
    >
      <ComboboxPrimitive.Positioner
        side={side}
        sideOffset={sideOffset}
        align={align}
        alignOffset={alignOffset}
        anchor={anchor}
        className="isolate z-50 pointer-events-auto"
      >
        <ComboboxPrimitive.Popup
          data-slot="combobox-content"
          data-chips={!!anchor}
          className={cn(
            "group/combobox-content relative grid max-h-100 grid-rows-[auto_minmax(0,1fr)_auto] w-(--anchor-width) max-w-(--available-width) min-w-[calc(var(--anchor-width)+--spacing(7))] origin-(--transform-origin) overflow-hidden rounded-md bg-popover text-popover-foreground shadow-md ring-1 ring-foreground/10 duration-100 data-[chips=true]:min-w-(--anchor-width) data-[side=bottom]:slide-in-from-top-2 data-[side=left]:slide-in-from-right-2 data-[side=right]:slide-in-from-left-2 data-[side=top]:slide-in-from-bottom-2 *:data-[slot=input-group]:m-2 *:data-[slot=input-group]:mb-0 *:data-[slot=input-group]:h-9 *:data-[slot=input-group]:border-input *:data-[slot=input-group]:bg-transparent *:data-[slot=input-group]:shadow-none data-open:animate-in data-open:fade-in-0 data-open:zoom-in-95 data-closed:animate-out data-closed:fade-out-0 data-closed:zoom-out-95",
            className,
          )}
          {...props}
        />
      </ComboboxPrimitive.Positioner>
    </ComboboxPrimitive.Portal>
  );
}

